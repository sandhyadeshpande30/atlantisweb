DROP TABLE LoyaltyCustomer IF EXISTS;

CREATE TABLE LoyaltyCustomer (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	userID VARCHAR(25) NOT NULL,
	lastName VARCHAR(50) NOT NULL,
	firstName VARCHAR(50) NULL,
	balance BIGINT NOT NULL,
	customMessage VARCHAR(100) NULL,
	PRIMARY KEY (id)
);



DROP TABLE LoyaltyTransaction IF EXISTS;

CREATE TABLE LoyaltyTransaction (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	customer_id BIGINT NOT NULL,
	type INTEGER not NULL,
	transactionDate TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
	transactionAmount numeric(19,2) NOT NULL,
	loyaltyPoints BIGINT NOT NULL,
	status VARCHAR(10) NOT NULL,
	transactionRemarks VARCHAR(100) NULL,
	PRIMARY KEY (id)
);